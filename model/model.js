export function modelPageName(pgName) {
  console.log(pgName);
  // $("#app").html(eval(pgName));

  // !make sure to use an operator "==" if checking
  if (pgName == "homeContent") {
    $("nav a").removeClass("black-text").addClass("white-text");
    $("div").removeClass("logo-black");
    $("div").removeClass("black-underline").addClass("white-underline");
  } else {
    $("nav a").removeClass("white-text").addClass("black-text");
    $("div").removeClass("logo-white");
    $("div").removeClass("white-underline").addClass("black-underline");
  }
  try {
    eval(pgName);
    $("#app").html(eval(pgName));
  } catch (e) {
    console.log("hello", e);
  }
}
